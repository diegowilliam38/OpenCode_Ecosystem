from dataclasses import dataclass
from datetime import datetime
import hashlib


@dataclass
class Problem:
    """Mathematical problem definition"""
    id: str
    domain: str  # algebra, logic, set_theory, analysis, etc.
    statement: str
    difficulty: str  # easy, intermediate, hard, research
    variables: List[str] = None
    

@dataclass
class ReasoningPlan:
    """Selected reasoning phases and strategies"""
    phases_selected: List[int]  # Phase 1-7
    reasoning_types: List[str]  # e.g., ["R01_Notation", "R02_Abstraction"]
    domain_strategy: str
    estimated_sorry_count: int


