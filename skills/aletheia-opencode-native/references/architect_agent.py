"""
Aletheia Architect Agent
Stage 1: Problem Analysis → Reasoning Phase Selection → Proof Skeleton Generation

Integrates:
- ReasoningOrchestrator-v11 (68 reasoning types, 7 phases)
- Domain-specific proof templates
- sequential-thinking MCP for reasoning transparency
- DecisionNode for strategy recording
"""

import json
